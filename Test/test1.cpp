int sumLessThanTarget(int* nums, int size, int target){
    int maxSum = 0;
    for(int j = 0; j < size; j++){
        int sum = 0;
        for(int i = j; i < size; i ++){
            int temp = target - nums[i];
            if(temp >= sum){
                sum += nums[i];
            }
        }
        if(maxSum <= sum) maxSum = sum;
    }

    return maxSum;
}

